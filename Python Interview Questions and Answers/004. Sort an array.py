#Sort an array 

#Ascending Order

#Solution 1
arr = [4,9,1,3,8,5]

result = sorted(arr)
print(result)

#Solution 2
arr = [4,9,1,3,8,5]

arr.sort()
print(arr)

#Descending Order

#Solution 1
arr = [4,9,1,3,8,5]

result = sorted(arr, reverse=True)
print(result)

#Solution 2
arr = [4,9,1,3,8,5]

arr.sort(reverse = True)
print(arr)
